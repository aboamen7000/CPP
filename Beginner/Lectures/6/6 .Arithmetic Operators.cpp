// Header file
#include <iostream>
// Arithmetic Operators.
// Arithmetic Operators : can use to perform mathematical operations on numerical values
using namespace std;
int main(){

    int x = 5;
    int y = 3;
    // creating a new variable to assignment the result of two values.

    // math Addition (+)
    int Addition = x + y;

    // math Substraction (-)
    int Substraction = x - y;

    // math Multiplication (*)
    int Multiplication = x * y;

    // math Division (/)
    int Division = x / y;

    // math Modulo (%) 1.6 >> 2
    int Modulo = x % y;

    // math Increment (++)
    // x++;

    // math Decrement (--)
    x--;

    cout << "Result is : " << x << endl;

    return 0; // function will end without any error
}